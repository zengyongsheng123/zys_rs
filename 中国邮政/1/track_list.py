def ease_in_out_quad(t, b, c, d):
    """缓入缓出二次函数，用于模拟自然加速和减速"""
    t /= d / 2
    if t < 1:
        return c / 2 * t * t + b
    t -= 1
    return -c / 2 * (t * (t - 2) - 1) + b


def generate_trajectory(target_x, total_time=800):
    """
    生成滑块验证码轨迹
    :param target_x: 目标滑动距离（像素）
    :param total_time: 总时间（微秒）
    :return: 轨迹列表
    """
    trajectory = []
    current_time = 0
    start_time = random.randint(10000, 100000)  # 随机起始时间戳
    current_x = 0
    current_y = 0
    step_interval = random.randint(3, 15)  # 初始时间间隔（微秒）

    # 添加按下事件
    trajectory.append({
        "x": current_x,
        "y": current_y,
        "type": "down",
        "t": start_time
    })

    while current_x < target_x and current_time < total_time:
        # 计算进度（0到1）
        progress = current_time / total_time
        # 使用缓动函数计算x
        eased_x = ease_in_out_quad(current_time, 0, target_x, total_time)
        # 添加随机噪声到x
        x_noise = random.uniform(-0.5, 0.5)
        next_x = min(eased_x + x_noise, target_x)

        # 更新y，模拟手抖
        y_change = random.uniform(-0.3, 0.3)
        current_y = max(min(current_y + y_change, 5), -5)  # 限制y范围
        current_y = round(current_y, 2)

        # 更新x
        current_x = round(next_x, 2)

        # 添加随机噪声到时间间隔
        time_noise = random.uniform(-2, 2)
        step_interval = max(3, min(50, step_interval + time_noise))
        current_time += step_interval
        current_timestamp = start_time + current_time

        # 添加移动事件
        trajectory.append({
            "x": current_x,
            "y": current_y,
            "type": "move",
            "t": int(current_timestamp)
        })

    # 添加释放事件
    trajectory.append({
        "x": target_x,
        "y": current_y,
        "type": "up",
        "t": int(start_time + total_time)
    })

    return trajectory


